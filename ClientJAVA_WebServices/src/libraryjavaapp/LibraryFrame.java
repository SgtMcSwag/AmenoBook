    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            URL url = new URL("http://localhost:8080/WebServicesProjet/api/rest/loans/add");
            Map<String,Object> params = new LinkedHashMap<>();
            params.put("duration", 2);
            params.put("isreturned", false);
            params.put("isreturned", false);
            Date date = new Date();
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            params.put("startdate", df.format(date));
            params.put("book", jLabel13.getText());
            params.put("libraryuser", idUser);

            StringBuilder postData = new StringBuilder();
            for (Map.Entry<String,Object> param : params.entrySet()) {
                if (postData.length() != 0) postData.append('&');
                postData.append(URLEncoder.encode(param.getKey(), "UTF-8"));
                postData.append('=');
                postData.append(URLEncoder.encode(String.valueOf(param.getValue()), "UTF-8"));
            }
            byte[] postDataBytes = postData.toString().getBytes("UTF-8");

            HttpURLConnection conn = (HttpURLConnection)url.openConnection();
            conn.setRequestMethod("POST");
            conn.setRequestProperty("Content-Type", "application/x-www-form-urlencoded");
            conn.setRequestProperty("Content-Length", String.valueOf(postDataBytes.length));
            conn.setDoOutput(true);
            conn.getOutputStream().write(postDataBytes);

            Reader in = new BufferedReader(new InputStreamReader(conn.getInputStream(), "UTF-8"));

            String jsonString = "";
            for (int c; (c = in.read()) >= 0;)
                jsonString += (char)c;
            
            JSONParser parser = new JSONParser();
            JSONObject json = (JSONObject) parser.parse(jsonString);
            String test = json.toJSONString();
            jLabel14.setText("Livre lou√© !");
        }catch(Exception e){
            jLabel14.setText("Erreur. Plus de livres disponibles ?");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

